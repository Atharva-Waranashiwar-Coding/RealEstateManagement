    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblBrokers.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Broker b = (Broker) tblBrokers.getValueAt(row, 0);
        ArrayList<UserAccount> list = business.getBusinessUserDirectory().getUserAccountList();
        Iterator<UserAccount> iterator = list.iterator();
        while (iterator.hasNext()) {
            UserAccount user = iterator.next();
            if (user.getUsername().equals(b.getUsername())) {
                iterator.remove(); // Remove the element using the iterator
            }
        }
        for (Enterprise ent : business.getEnterpriseDirectory().getEnterpriseList()) {
            if (ent.getName().equals(tblBrokers.getValueAt(row, 1))) {
                for (Organisation org : ent.getOrganisationDirectory().getOrganisationList()) {
                    if (org instanceof PropertyManagementOrganisation) {
                        PropertyManagementOrganisation pmo = (PropertyManagementOrganisation) org;
                        pmo.getEmployeeDirectory().getEmployeeList().remove(b);
                    }
                }
            }
        }
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

