    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblBrokers.getModel();
        model.setRowCount(0);
        EnterpriseDirectory ed=this.business.getEnterpriseDirectory();
        for(Enterprise enterprise:ed.getEnterpriseList()) {
            if(enterprise instanceof RealEstateAgencyEnterprise realEstateEnterprise){
                for(Broker broker:realEstateEnterprise.getAllBrokers()){
                    Object row[] = new Object[2];
                    row[0] = broker;
                    row[1] = realEstateEnterprise.getName();
                    model.addRow(row);
                }
            } 
        }
    }
