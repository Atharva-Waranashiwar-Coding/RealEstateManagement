    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblViewers.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Viewer v = (Viewer) tblViewers.getValueAt(row, 0);
        ArrayList<UserAccount> list = business.getBusinessUserDirectory().getUserAccountList();
        Iterator<UserAccount> iterator = list.iterator();
        while (iterator.hasNext()) {
            UserAccount user = iterator.next();
            if (user.getUsername().equals(v.getUsername())) {
                iterator.remove(); // Remove the element using the iterator
            }
        }
        for (Enterprise ent : business.getEnterpriseDirectory().getEnterpriseList()) {
            if (ent.getType().equals("Viewer")) {
                for (Organisation org : ent.getOrganisationDirectory().getOrganisationList()) {
                    if (org instanceof ViewerOrganisation viewer) {
                        viewer.getViewerDirectory().getViewerList().remove(v);
                    }
                }
            }
        }
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


