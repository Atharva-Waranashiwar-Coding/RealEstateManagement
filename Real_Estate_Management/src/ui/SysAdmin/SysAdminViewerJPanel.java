    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblViewers.getModel();
        model.setRowCount(0);
        EnterpriseDirectory ed=this.business.getEnterpriseDirectory();
        for(Enterprise enterprise:ed.getEnterpriseList()) {
            if(enterprise instanceof ViewerEnterprise viewerEnterprise){
                for(Organisation org:viewerEnterprise.getOrganisationDirectory().getOrganisationList()){
                    ViewerOrganisation viewerOrg = (ViewerOrganisation) org;
                    for(Viewer viewer:viewerOrg.getViewerDirectory().getViewerList()){
                        Object row[] = new Object[2];
                        row[0] = viewer;
                        row[1] = viewer.getMyOffers().size();
                        model.addRow(row);
                    }
                }
            } 
        }
    }

